	private String title;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLatingx() {
		return latingx;
	}
	public void setLatingx(String latingx) {
		this.latingx = latingx;
	}
	public String getLatingy() {
		return latingy;
	}
	public void setLatingy(String latingy) {
		this.latingy = latingy;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getMapdate() {
		return mapdate;
	}
	public void setMapdate(String mapdate) {
		this.mapdate = mapdate;
	}
	
	
	
	
